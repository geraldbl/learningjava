package basics;

public class Days {
	public static void main(String[] args) {
		String dayOfWeek = "Tues";
		switch (dayOfWeek) {
		case "Mon" : 
			System.out.println("Today is Monday");
			break;
		case "Tues" : 
			System.out.println("Today is Tuesday");
			break;
		case "Wed" : 
			System.out.println("Today is Wednesday");
			break;

		}
	}
}
